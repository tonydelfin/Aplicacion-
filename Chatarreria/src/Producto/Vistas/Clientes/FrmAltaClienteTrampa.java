    private void txtTrampaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTrampaKeyTyped
        InputMap map2 = this.txtTrampa.getInputMap(JTextField.WHEN_FOCUSED);
        map2.put(KeyStroke.getKeyStroke(KeyEvent.VK_V, Event.CTRL_MASK),"null");

        
        char c  = evt.getKeyChar();
        
        if(!Character.isDigit(c) && c!= '.'){
                    evt.consume();
                
        } if(c == '.' && txtTrampa.getText().contains(".")){
                    evt.consume();
        }
      
    }//GEN-LAST:event_txtTrampaKeyTyped

