    private void num(JTextField a)
    {
        a.addKeyListener(new KeyAdapter(){
            public void KeyTyped(KeyEvent e){
                char c  = e.getKeyChar();
                if(!Character.isDigit(c) && c!= '.'){
                    e.consume();
                }
                if(c == '.' && txtTrampa.getText().contains(".")){
                    e.consume();
                }
            }
        });
    }

