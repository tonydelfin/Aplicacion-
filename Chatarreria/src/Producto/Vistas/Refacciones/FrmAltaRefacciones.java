    private void btnGuaradarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuaradarActionPerformed
        if (this.altaRefaccion == true){
         if((this.txtPrecio.getText().length() >0) && (this.txtDescripcion.getText().length() >0) && !(this.txtDescripcion.getText().trim().isEmpty())
                 && !(this.txtPrecio.getText().trim().isEmpty())){
                producto r = new producto();
              r.ponerIdproducto(0);
              r.ponerDescripcion(this.txtDescripcion.getText().trim());
              r.ponerPrecio(Double.parseDouble(this.txtPrecio.getText().trim()));
     
       DAOProductos daorefaccion = new DAOProductos();
       Integer re = daorefaccion.agregarProducto(r);
        if(re == 1){
            JOptionPane.showMessageDialog(null, "Producto registrado correctamente","Aviso", JOptionPane.INFORMATION_MESSAGE);
        }else{
             JOptionPane.showMessageDialog(null, "Ocurrio un fallo al hacer el registro del Produco ","Error", JOptionPane.INFORMATION_MESSAGE);
        }
        this.setVisible(false);
     }
         else{
             JOptionPane.showMessageDialog(null, "Los datos estan Incorrectos, intente otra vez", "Error", JOptionPane.ERROR_MESSAGE);
         }
     
        } 
     else{
            if((this.txtPrecio.getText().length() >0) && (this.txtDescripcion.getText().length() >0) && !(this.txtDescripcion.getText().trim().isEmpty())
                 && !(this.txtPrecio.getText().trim().isEmpty())){
                producto r = new producto();
                r.ponerIdproducto(Long.parseLong(this.lblIdProducto.getText().trim()));
                r.ponerDescripcion(this.txtDescripcion.getText().trim());
                r.ponerPrecio(Double.parseDouble(this.txtPrecio.getText().trim()));

                DAOProductos daorefaccion = new DAOProductos();
                Integer re = daorefaccion.actualizacionProducto(r);
               if(re == 1){
                   JOptionPane.showMessageDialog(null, "Producto actualizado correcramente","Aviso", JOptionPane.INFORMATION_MESSAGE);
               }else{
                    JOptionPane.showMessageDialog(null, "Ocurrio un fallo al hacer el registro del Produco ","Error", JOptionPane.INFORMATION_MESSAGE);
               }
               this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Los datos estan Incorrectos, intente otra vez", "Error", JOptionPane.ERROR_MESSAGE);
            }
         
     
        }

    }//GEN-LAST:event_btnGuaradarActionPerformed

