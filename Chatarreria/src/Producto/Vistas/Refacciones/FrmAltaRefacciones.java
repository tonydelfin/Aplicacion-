    private void txtDescripcionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescripcionKeyTyped
        InputMap map2 = this.txtDescripcion.getInputMap(JTextField.WHEN_FOCUSED);
        map2.put(KeyStroke.getKeyStroke(KeyEvent.VK_V, Event.CTRL_MASK),"null");

        char letra = evt.getKeyChar();
        if((letra < 'a' || letra > 'z') && (letra < 'A' || letra > 'Z') && (letra != 32) && (letra != '\u00f1') && (letra != '\u00d1')
        && (letra != '\u002E') && (letra != 'á') && (letra != 'é') && (letra != 'í') && (letra != 'ó') && (letra != 'ú') && 
        (letra != 'Á') && (letra != 'É') && (letra != 'Í') && (letra != 'Ó') && (letra != 'Ú') && (letra != 8)){
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
        if(this.txtDescripcion.getText().length() >= 30){
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
        char txtDescripcion=  evt.getKeyChar();
        if(Character.isDigit(txtDescripcion)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane,"Ingrese solo Letras");
        }
    }//GEN-LAST:event_txtDescripcionKeyTyped

